/*
			简介
			Node是JavaScript语言的服务器运行环境。
			所谓“运行环境”有两层意思：
			首先，JavaScript语言通过Node在服务器运行，在这个意义上，Node有点像JavaScript虚拟机；
			其次，Node提供大量工具库，
			使得JavaScript语言与操作系统互动（比如读写文件、新建子进程），
			在这个意义上，Node又是JavaScript的工具库。
			Node内部采用Google公司的V8引擎，作为JavaScript语言解释器；
			通过自行开发的libuv库，调用操作系统资源。
*/

/*
        NodeJs和JavaScript的相同点：

        他们都是基于ECMAScript,即语法、内置对象、方法相同。
        
        NodeJs和JavaScript的不同点：
         JavaScript-------浏览器
         NodeJs-------os系统、net网络、database数据库。。。。。

        JavaScript的顶层对象是Window.
        NodeJs的顶层对象是Global
*/
   